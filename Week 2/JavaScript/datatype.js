// Data Types (Primitice Data Types)
/* 
    1. Numbers : represent numeric values (eg. intergers, whole numbers)
    2. Strings : represent character values (eg. "Name", "Age")
                    enclosed in " " or ' '
    3. Boolean : represent true or false 
    4. Null : represent intentional absence of value
    5. Undefined : represents unintentional absence of value
*/ 

// Example for number
let age = 25;
console.log("The value of age is: ", age);
console.log("The type of variable age: ", typeof age);

// Example for string
// Deprecated variables can be use in the code but not advisable
let name = "Jeomarie";
console.log("The value of name is: ", name);
console.log("The type of variable name: ", typeof name);

// Example for boolean
let isStudent = true;
let isTeacher = false;
console.log("The value of isStudent: ", isStudent);
console.log("The datatype of variable isStudent: ", typeof isStudent);

// Example of null
// returns an "object" data type
let car = null;
console.log("The value of car is: ", car);
console.log("The type of variable car: ", typeof car);

// Example for undefined
let city;
console.log("The value of city is: ", city);
console.log("The type of variable city: ", typeof city);